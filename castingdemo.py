# int(): Converts to an integer.
# float(): Converts to a float.
# str(): Converts to a string.
# list(): Converts to a list.
# tuple(): Converts to a tuple.
# set(): Converts to a set.

# Convert string to number
str_number = "25"
num = int(str_number)
print(f"String to integer: {num}")

# Convert integer to float
int_number = 50
num_float = float(int_number)
print(f"Integer to float: {num_float}")

# Convert float to integer (round down)
float_number = 12.99
num_int = int(float_number)
print(f"Float to integer: {num_int}")

# Convert list to string
my_list = ['a', 'b', 'c']
list_str = ''.join(my_list)  # join list elements into a string
print(f"List to string: {list_str}")
